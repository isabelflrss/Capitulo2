public class Ejercicio1 {
    
    public String pa(Integer a,Integer b,Integer c){
        return String.format("Ejercicio 1 a): %d",a+b*c);
    }
    public String pb(float a,float b, float c){
        return String.format("Ejercicio 1 a): %.2f",a/b+c);
    }
    public String pc(float a,float b, float c,float d){
        return String.format("Ejercicio 1 c): %.2f",a/b+c/d);
    }
    public String pd(float a, float b){
        return String.format("Ejercicio 1 d): %.2f",a/b);
    }
    public String pe(float a, float b){
        return String.format("Ejercicio 1 e): %.2f",a/b);
    }
    public String pf(float a, float b){
        return String.format("Ejercicio 1 f): %.2f",a/b);
    }
    public String pg(float a, float b){
        return String.format("Ejercicio 1 g): %.2f",a%b);
    }
    public String ph(float a, float b){
        return String.format("Ejercicio 1 h): %.2f",a%b);
    }
    public String pi(float a, float b){
        return String.format("Ejercicio 1 i): %.2f",a%b);
    }
    public String pj(float a,float b, float c){
        return String.format("Ejercicio 1 j): %.2f",a%b+c);
    }
    public String pk(float a,float b, float c){
        return String.format("Ejercicio 1 k): %.2f",(a+b)*c);
    }
    public String pl(float a,float b, float c){
        return String.format("Ejercicio 1 l): %.2f",a/(b+c));
    }
    public String pm(float a, float b){
        return String.format("Ejercicio 1 m): %.2f",a%b);
    }

}
